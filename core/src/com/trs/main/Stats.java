package com.trs.main;

public class Stats {
	private int level;
	private int hp;
	private int def;
	private int atk;
	private int init;
	
	public Stats() {
		
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getHp() {
		return hp;
	}

	public void setHp(int hp) {
		this.hp = hp;
	}

	public int getDef() {
		return def;
	}

	public void setDef(int def) {
		this.def = def;
	}

	public int getAtk() {
		return atk;
	}

	public void setAtk(int atk) {
		this.atk = atk;
	}

	public int getInit() {
		return init;
	}

	public void setInit(int init) {
		this.init = init;
	}
	
}
